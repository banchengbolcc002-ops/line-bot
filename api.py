# ✅ 回覆 LINE 訊息函式
# ==========================================
def reply_to_line(reply_token, text):

    # ✅ LINE需要的標頭（驗證身份）
    headers = {
        "Authorization": "Bearer " + LINE_TOKEN,
        "Content-Type": "application/json"
    }

    # ✅ 發送格式（LINE規定）
    data = {
        "replyToken": reply_token,
        "messages": [
            {
                "type": "text",
                "text": str(text)
            }
        ]
    }

    # ✅ 傳送給 LINE
    requests.post(
        "https://api.line.me/v2/bot/message/reply",
        headers=headers,
        json=data
    )


